public class day_thirtyseven_coding {
    
    public static void main(String[] args) {
        System.out.println("Operator penugasan");
        int tabungan;
        // pengisian nilai (=)
        tabungan = 20000;
        System.out.println("Isi tabunganku :" + tabungan);
        
        // pengisian dan penjumlahan (+=)
        tabungan += 2000;
        System.out.println("Isi tabunganku setelah ditambah :" + tabungan);
        
        // pengisian dan pengurangan (-=)
        tabungan -= 12000;
        System.out.println("Sisa tabungan ku setelah diambil :" + tabungan);
    }
    
}

public class day_fiftynine_coding {
    
    public static void main(String[] args) {
        System.out.println("Looping");
        int[] a = {1, 2, 3, 4, 5};

        
        for (int angka : a) {
                System.out.print(angka);
                System.out.print(" ");
            }
        
    }
}

public class day_thirtyeight_coding {
    
    public static void main(String[] args) {
        System.out.println("Operator penugasan");
        double nilai = 90.50;
        System.out.println("Nilai awal : " + nilai);
        
        // pengisian dan perkalian
        nilai *= 5;
        System.out.println("Nilai setelah di kali : " + nilai);
        
        // pengisian dan pembagian
        nilai /= 2;
        System.out.println("Nilai setelah di bagi : " + nilai);
    }
    
}

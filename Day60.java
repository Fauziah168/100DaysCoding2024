public class day_sixty_coding {
    
    public static void main(String[] args) {
        System.out.println("Looping");
        int a = 1;
        
        do {
            System.out.println("selama malam");
            System.out.println("semoga bahagia");
            System.out.println(" ");
            a++;
        } while (a <= 5);
    }
    
}

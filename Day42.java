public class day_fortytwo_coding {
    
    public static void main(String[] args) {
        System.out.println("Operator Perbandingan");
        byte h = 8;
        byte i = 2;
        
        // Sama dengan (=)
        System.out.println(h == i);
        // Tidak Sama dengan (!=)
        System.out.println(h != i);
    }
    
}

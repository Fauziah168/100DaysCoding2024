public class day_eightytwo_coding {
    
    public static void main(String[] args) {
        double a = 3.6;
        
        double ceil = Math.ceil(a);
        System.out.println("Hasil dari ceil : " + ceil);
        double floor = Math.floor(a);
        System.out.println("Hasil dari floor : " + floor);
        double round = Math.round(a);
        System.out.println("Hasil dari round : " + round);
        
        double aNegatif = -3.6;
            System.out.println("Hasil dari Ceil dengan angka negatif : " + Math.ceil(aNegatif));
    }
    
}

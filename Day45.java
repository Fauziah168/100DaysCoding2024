public class day_fortyfive_coding {
    
    public static void main(String[] args) {
        System.out.println("Operator Logika");
        boolean a, b, c, d;
        a = true;
        b = true;
        c = false;
        d = false;
        
        // OR ( || )
        System.out.println(a || b);
        System.out.println(a || c);
        System.out.println(d || b);
        System.out.println(c || d);
    }
    
}
